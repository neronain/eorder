function RefreshRemoveTeethImage(index){
	var text ="";
	text += "";
	text +='<table width="100%" height=100% border="0" cellpadding="0" cellspacing="0" align=center><tr><td ';	
	text += "onclick=\"OnClickRemoveTeeth("+index+")\"  style=\"cursor:pointer\" >";
	if(index==32){
		text+= html_teeth_imageRemoveType(g_RemoveUpperType,g_RemoveUpperOption1,g_RemoveUpperOption2,0);
		VerifyRemoveDetailDisplay("Upper");
	}else if(index==33){
		text+= html_teeth_imageRemoveType(g_RemoveLowerType,g_RemoveLowerOption1,g_RemoveLowerOption2,g_RemoveLowerOption3);
		VerifyRemoveDetailDisplay("Lower");
	}else{
		text += html_teeth_imageRemove(g_RemoveTeeth_list[index]);
	}
	text += "</td></tr></table>";
	writeit("RemoveTeethImage"+index,text);
}

function html_teeth_image(teeth){
	if(teeth.material==0){
		return '<img src="../resource/images/eorder/teeth/number/select.gif" width="56" height="56" />'
	}
	var text ="";
	var arrayImage = new Array();
	var tmpArray = getTeethMaterialImageArray(v_method,teeth.material);
	var i;
	for(i=0;i<tmpArray.length;i++){
		arrayImage[i] = tmpArray[i];
	}
	var tmpArray = getTeethMaterialImageArray(v_method,teeth.pintooth);
	for(var j=0;j<tmpArray.length+i;j++){
		arrayImage[i+j] = tmpArray[j];
	}
	
	text+= html_teeth_image_lowlevel(arrayImage);
	
	return text;
}
function html_teeth_imageRemoveType(type,option1,option2,option3){
	if(type==REMOVETYPE.NONE){
		return '<img src="../resource/images/eorder/teeth/number/select.gif" width="56" height="56" />'
	}
	var text ="";
	text += '<img src="../resource/images/eorder/teeth/teeth_imagefile/'+getteeth_imagefile((type+9000))+'.gif" width="56" height="56" />';
	if(option1>0){
		text += ' <img src="../resource/images/eorder/teeth/teeth_imagefile/'+getteeth_imagefile((option1+9004))+'.gif" width="56" height="56" />';
	}
	if(option2>0){
		text += ' <img src="../resource/images/eorder/teeth/teeth_imagefile/'+getteeth_imagefile((9007))+'.gif" width="56" height="56" />';
	}
	if(option3>0){
		text += ' <img src="../resource/images/eorder/teeth/teeth_imagefile/'+getteeth_imagefile((9008))+'.gif" width="56" height="56" />';
	}
	return text;
}
function html_teeth_imageRemove(teeth){
	if(teeth.material==0 && teeth.rest==0){
		return '<img src="../resource/images/eorder/teeth/number/select.gif" width="56" height="56" />'
	}
	var text ="";
	var arrayImage = new Array();
	var tmpArray = getTeethRemoveMaterialImageArray(teeth.material);
	var i=0;
	for(i=0;i<tmpArray.length;i++){
		arrayImage[i] = tmpArray[i];
	}
	tmpArray = getTeethRemoveMaterialImageArray(teeth.rest);
	for(var j=0;j<tmpArray.length+i;j++){
		arrayImage[i+j] = tmpArray[j];
	}	
	text+= html_teeth_image_lowlevel(arrayImage);
	return text;
}
function BuildFixSelecterImage(index){
	var id = index;

	if(getTeethMaterialImageArray(v_method,id).length==0){
		writeit("FixSelecterMaterialImage"+index,"&nbsp;");
		return;
	}

	var text ="";
	text += "";
	text +='<table width="100%" height=100% border="0" cellpadding="0" cellspacing="1" align=center'
	
	if(id==selected_material_id){
		text +=' bgcolor="#000000" ';
		text +='><tr onclick="OnClickFixSelectorMaterial('+id+')"  style="cursor:pointer" ';
		text +='><td bgcolor="#FFFFFF" width=62 align="center"  valign = "middle">';	
	}else{
		text +='><tr onclick="OnClickFixSelectorMaterial('+id+')"  style="cursor:pointer" ';
		text +='><td width=62 align="center" valign = "middle">';	
	}
	text += html_material_image(v_method,id);
	text += '</td><td  align="center" ';
	if(id==selected_material_id){
		text += ' bgcolor="#FFFFFF" >';
	}else{
		text += ">";
	}
	text += getTeethName(id);
	text += "</td></tr></table>";
	
	
	writeit("FixSelecterMaterialImage"+index,text);
	

}

