function html_teeth_image_lowlevel(arImageID){
	if(arImageID.length==0) return "";
	var arImageID2 = new Array();
	for(var i=0;i<arImageID.length-1;i++){
		arImageID2[i] = arImageID[i];
	}
	if(arImageID[arImageID.length-1]==0){
		return html_teeth_image_lowlevel(arImageID2);
	}
	// html_teeth_image_lowlevel(new array(001,002,004,225,3423,239));
	var text = "";
	text += '<table border="0" cellpadding="0" cellspacing="0"><tr><td width=56 height=56 '
	text += ' background = "../resource/images/eorder/teeth/teeth_imagefile/'+getteeth_imagefile(arImageID[arImageID.length-1])+'.gif" >';
	text += html_teeth_image_lowlevel(arImageID2);
	text += "</td></tr></table>";	 
	return text;
}